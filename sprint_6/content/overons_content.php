    <div class="col-s-4 divje"><h2>Heden</h2>
    <img src="images/heden.jpg" class="tijd">
  <br>
  <br>
  <p class="ptekst"> In de huidige tijd liggen de werkzaamheden van Spyservices voor een groot deel op het digitale vlak. Het infiltreren van computernetwerken en ontregelen van systemen behoort tot de dagelijkse werkzaamheden van ons bedrijf en de ontwikkelingen van internet in de afgelopen jaren hebben het marktaandeel van “digitale spionage” alleen maar groter gemaakt. </p>
  <p class="ptekst"> Ook het verzamelen van informatie binnen en buiten onze landsgrenzen behoort nog steeds tot de dagelijkse werkzaamheden van ons bedrijf. </p>
  <p class="ptekst"> Op dit moment zijn er nog ongeveer 35 mensen in dienst, waarvan ruim 20 spionnen (binnen- en buitendienst) en daarnaast nog veel administratief personeel voor het verwerken van alle verkregen informatie. </p>  
   </div>
