    <div class="col-s-4 divje"><h2>Geschiedenis</h2>
  <img src="images/past.jpg" class="tijd">
  <br>
  <br>
  <p class="ptekst"> Het bedrijf Spyservices is in 1935 opgericht door Erik Hazelnoot.Hij noemde het bedrijf in eerste instantie Spionnendienst Nederland BV, maar door de dreiging van de Tweede wereldoorlog werden de werkzaamheden al snel uitgebreid naar het buitenland. Hazelnoot vond daarom een bedrijfsnaam met wat meer internationale allure beter passen bij het bedrijf. </p>
  <p class="ptekst"> In de beginjaren werkte Erik vooral alleen, maar omdat het werk al snel teveel werd nam hij in 1939 twee collega spionnen in dienst. Ook na de Tweede Wereldoorlog bleef het bedrijf groeien en al snel waren er meer dan 50 mensen in dienst. De hoogtijdagen van het bedrijf lagen in de jaren ’80 van vorige eeuw, toen de Koude Oorlog met Rusland zijn hoogtepunt bereikte. </p> </p>
